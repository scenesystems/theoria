/**
 * Numeric hyperbolic and inverse hyperbolic kernels.
 *
 * @since 0.3.0
 * @category internal
 */

/**
 * Hyperbolic sine on real scalar inputs.
 *
 * @since 0.3.0
 * @category internal
 */
export const sinh: (value: number) => number = Math.sinh

/**
 * Hyperbolic cosine on real scalar inputs.
 *
 * @since 0.3.0
 * @category internal
 */
export const cosh: (value: number) => number = Math.cosh

/**
 * Hyperbolic tangent on real scalar inputs.
 *
 * @since 0.3.0
 * @category internal
 */
export const tanh: (value: number) => number = Math.tanh

/**
 * Inverse hyperbolic sine on real scalar inputs.
 *
 * @since 0.3.0
 * @category internal
 */
export const asinh: (value: number) => number = Math.asinh

/**
 * Inverse hyperbolic cosine on real scalar inputs.
 *
 * @since 0.3.0
 * @category internal
 */
export const acosh: (value: number) => number = Math.acosh

/**
 * Inverse hyperbolic tangent on real scalar inputs.
 *
 * @since 0.3.0
 * @category internal
 */
export const atanh: (value: number) => number = Math.atanh
